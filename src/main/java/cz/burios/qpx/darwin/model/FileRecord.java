package cz.burios.qpx.darwin.model;

public class FileRecord {
	
	private String id;
	private String fileName;

	// ----- Getters / Setters ------------------------------------------------
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
}